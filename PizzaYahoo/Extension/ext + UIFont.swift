import UIKit

extension UIFont {
    
    static func sfProDisplay(size: CGFloat) -> UIFont? {
        UIFont(name: "SFProDisplay-Medium", size: size)
    }
    
    static func headlineText(size: CGFloat) -> UIFont? {
        UIFont(name: "Headlinetext", size: size)
    }
}
